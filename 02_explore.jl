# ╔═╡ 0ebc3a6a-ab21-4ab6-8d77-94ce7a7b0b4d
Integer <: Real

# ╔═╡ 44c0ecc8-83af-42f3-97e5-2f1e3ebb075d
Integer <: Float64

# ╔═╡ 7f51f165-3f11-4d33-adae-fa79b0750dc9
md"""
In Python, you would want to do something like this to select the proper columns:

typevector = df.dtypes == np.float64
`df.loc[:, typevector]`

Here, df.dtypes == np.float64` will give you a boolean array with true and false values. The `.loc[]` format let's you select the correct columns.

In Julia, the approach would be like this.
"""

# ╔═╡ 363d915a-1eec-468b-85f7-dbbfb97ff59c
typevector = isfloatvec.(eachcol(data))

# ╔═╡ b76026ce-2248-4f6d-8753-c26f9b20c961
floatdata = select(data, typevector)

# ╔═╡ eef29a52-5e0f-4062-9f02-a92678365859
md"""
Note how I use the . after the function. This is Julia syntax for broadcasting.
This also happens in [Python](https://numpy.org/doc/stable/user/basics.broadcasting.html), only there you might have never noticed that it happens.

It simply means: apply the function to every element of the next thing.
e.g if you want the `sin` of every number in a vector, you would do:

x = [1.4, 2.7, 0.3]
sin.(x)

which is more explicit than the implicit broadcasting.

If you really insist on the more pythonic syntax, you could have also used the julia syntax that is equivalent to the .loc syntax: `data[!, typevector]` but I think the select is clearer.

Also, for a full side-by-side comparison, look [here](https://dataframes.juliadata.org/stable/man/comparisons/#Comparison-with-the-Python-package-pandas)
"""

# ╔═╡ c4a94bab-1497-4206-b516-556bf0b0c3e7
function normalize(col)
	μ = Statistics.mean(col)
	σ = Statistics.std(col)
	(col .- μ) / σ
end

# ╔═╡ 315974a0-9f5a-4f36-8371-59da51f69828
normdata = transform(floatdata, names(floatdata) .=> normalize .=> names(floatdata))

# ╔═╡ e718dea8-a74a-447d-b6be-9b8ee385aea5
md"""
So, this was how I normalized the data.
Technically, you would not need to do `Statistics.mean`, but I think it is clearer because you might not have a clue that those functions come from the `Statistics` package.

Note that I didnt use the return keyword in the function. By default, the function returns the last line, the return keyword is optional (even though I think it is clearer to include it).

So, now we make our first plot. Let's do a kernel density estimate:
"""

# ╔═╡ 528e4767-79c0-440d-b5b7-9602eaf32afe
StatsPlots.density(normdata.culdep, group=data.species)

# ╔═╡ 2e14cb74-9c47-4c09-af82-6e8c29c0821e
md"""
Pretty much what you would expect, I think.

Let's do that for every column from our floatdata.

We will create a plotlist Vector, and with syntax that is pretty close to Python
you enumerate through the columnnames.

`push!` is the Julia equivalent of `plotlist.append(item)`
We are just adding plots to the vector, because why not.

"""

# ╔═╡ cd37ebc3-32c4-47dc-a4c0-6788374b25b9
function subplots(df::DataFrame, group::Vector)
	plotlist = []
	colnames = names(df)
	for (i, col) in enumerate(eachcol(df))
		push!(plotlist, StatsPlots.density(col, group=group, title=colnames[i]))
	end
	plotlist
end

