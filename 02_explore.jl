# ╔═╡ 206c0081-6961-4edd-9b69-886c46a51fff
function isfloatvec(col)
	typeof(col) <: Vector{Float64}
end

# ╔═╡ 1fc3d6d8-2b33-4b7e-934a-79c88bf5b87c
md"""
This function will return true if col is of type Vector{Float64}, or a subtype of that.

In general, `<:` means "is a subtype of".

This syntax could be useful if you  would want to include the integers as well: you could write `<: Vector{Real}`

To give you an idea of the hierarchy of types, have a look at the cells below.
If you want to read more, look [here](https://docs.julialang.org/en/v1/manual/types/)
"""

# ╔═╡ b633b6e7-2977-42aa-b69d-cc3519cc2f35
Real <: Number

# ╔═╡ 7fb07576-9c01-438f-ba4e-ca0ee9f2d2ff
